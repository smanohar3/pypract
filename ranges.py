#range(x) - single argument
print('My name is')
for i in range(5):
    print('Jimmy Five Times ' + str(i))

#range(x, y) - two arguments - starts with 2, ends with 8
print('My name is')
for i in range(2, 9):
    print('Jimmy Five Times ' + str(i))

#range(x, y) - three arguments - starts with 10, up to 20, with step of 2
print('My name is')
for i in range(10, 20, 2):
    print('Jimmy Five Times ' + str(i))

#range(x, y) - three arguments - starts with 5, up to -1, with step of -1
print('My name is')
for i in range(5, -1, -1):
    print('Jimmy Five Times ' + str(i))


